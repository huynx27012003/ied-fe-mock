export function translateUi(language, key, params = {}) {
  let text = UI_MESSAGES[language]?.[key] || UI_MESSAGES["en-vi"]?.[key] || key;
  Object.keys(params).forEach((param) => {
    text = text.replace(`{${param}}`, params[param]);
  });
  return text;
}

export function installI18n(app, store) {
  app.config.globalProperties.$tUi = (key, params = {}) => translateUi(store.state.language, key, params);
}
